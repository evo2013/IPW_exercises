! or ? - meaning depends on its placement in an expression or method.

1. != means NOT equal to
#Use it to check 1 expression is not equal to another expression

2. ! before something means NOT or negate. So !user_name means NOT the user_name

3. ! afer something means destructive. So words.uniq! would find all the unique items
in words and return those values for words. The original values of words woould be destroyed

4. ? beore something does not exist in Ruby. THere is ? : ternary IF - ELSE operator.

5. ? after something is like asking a question. It returns a Boolean value True or False

6. !! before something means get Boolean value of that object